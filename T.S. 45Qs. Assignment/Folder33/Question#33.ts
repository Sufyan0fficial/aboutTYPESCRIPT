let numberArray: number[] = [];
for (let i = 1; i < 10; i++) {
  numberArray.push(i);
}
for (let val of numberArray) {
  if (val == 1) console.log("first");
  else if (val == 2) console.log("second");
  else if (val == 3) console.log("third");
  else if (val == 4) console.log("forth");
  else if (val == 5) console.log("fifth");
  else if (val == 6) console.log("sixth");
  else if (val == 7) console.log("seventh");
  else if (val == 8) console.log("eighth");
  else if (val == 9) console.log("ninth");
}
